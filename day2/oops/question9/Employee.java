package day2.oops.question9;

public class Employee {
	    // Attributes of Employee class
	    String name;
	    double salary;

	    // Constructor to initialize name and salary
	    public Employee(String name, double salary) {
	        this.name = name;
	        this.salary = salary;
	    }

	    // toString method to return Employee's information
	    public String toString() {
	        return "Name: " + name + ", Salary: " + salary;
	    }
	}

